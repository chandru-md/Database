SELECT CONCAT(AUTHOR_FNAME,' ',AUTHOR_LNAME) AS AUTHOR,
CASE
WHEN COUNT(*) = 1 THEN '1 BOOK'
ELSE 'MULTIPLE BOOKS'
END AS COUNT
FROM BOOKS
WHERE AUTHOR IS NOT NULL
GROUP BY AUTHOR; 


