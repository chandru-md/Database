CASE
WHEN COUNT(*) = 1 THEN '1 BOOK'
ELSE CONCAT(COUNT(*),' BOOKS')
END AS COUNT
FROM BOOKS
WHERE AUTHOR_LNAME IS NOT NULL AND AUTHOR_FNAME IS NOT NULL
GROUP BY AUTHOR_FNAME,AUTHOR_LNAME; 

